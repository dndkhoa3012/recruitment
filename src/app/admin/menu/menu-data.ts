// --- Mock Service Implementation ---

let currentMenus = [...initialMenuItems];

// Simulate API delay
const delay = (ms: number) => new Promise(resolve => setTimeout(resolve, ms));

export const getMenus = async () => {
    await delay(300);
    return [...currentMenus];
};

export const createMenu = async (item: any) => {
    await delay(300);
    const newItem = { ...item, id: item.id || `item-${Date.now()}` };
    currentMenus = [newItem, ...currentMenus];
    return newItem;
};

export const updateMenu = async (updatedItem: any) => {
    await delay(300);
    currentMenus = currentMenus.map(item => item.id === updatedItem.id ? updatedItem : item);
    return updatedItem;
};

export const deleteMenu = async (id: string) => {
    await delay(300);
    currentMenus = currentMenus.filter(item => item.id !== id);
};
